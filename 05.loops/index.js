console.log("Loops In JavaScript")



// for Loops
username="jamshed"
for (i=0;i<3;i++){
    console.log(`${username} `+i)
}

let a=2
for (i=1;i<=10;i++){
    console.log("2x"+i+"="+a*i)
}



// while Loop
let j=0
while (j<10){
    console.log(j)
    j++
}


// Do-While Loop
k=0
do{
    console.log(k)
    k++;
}while(k<15)